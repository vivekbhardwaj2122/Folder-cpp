#include <iostream>
using namespace std;

int main() {
    float radius = 3;
    float length = 5, width = 4;
    float base = 6, height = 3;

    cout << "Area of Circle = " << 3.14 * radius * radius << endl;
    cout << "Area of Rectangle = " << length * width << endl;
    cout << "Area of Triangle = " << 0.5 * base * height << endl;
    return 0;
}
